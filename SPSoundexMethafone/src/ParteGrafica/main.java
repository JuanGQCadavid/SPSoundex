    /**
     * Buscar en la Bace de datos
     * (Lista) todos los nodos pareciddos
     * foneticamnte y generar un ArrayList
     * con los resultados obtendiso
     * @param evt lector de eventos.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Limpiamos los valores de jTextArea2
        jTextArea2.setText("");
        jTextArea2.repaint();
        
        //Creamos el ArrayList resultante.
        ArrayList<Nodo> resultado = null;
        
        //Si ya tenemos el Nodo en nuestra memoria
        if(nodoSoundex != null){
            //Lo guardamos ene l historial
             historialNodos.add(nodoSoundex);
             
             //Generamos el ArrayList
             resultado=spsSoundex.obtenerSoundex(nodoSoundex,(Integer)(jSpinner1.getValue()));
            
        }else{
            //leemos lo ingresado por el usuario
            String nodoText = jTextField1.getText();
            String nodoCodigo = soundex.metaphone(nodoText);
            
            //Buscamos el nodo
            Nodo nodo = spsSoundex.buscarNodo(nodoText);
            
            
            //Si el nodo es nulo, retornamos error
            if(nodo!= null){
                //Lo agregamos al historial.
                historialNodos.add(nodo);
                //generamos el ArrayList
                resultado = spsSoundex.obtenerSoundex(spsSoundex.buscarNodo(nodoText), (Integer)(jSpinner1.getValue()));
            }else{
                //Mensaje de error.
                JOptionPane.showMessageDialog(this, "Por favor, busque cosas coherentes.");
            }
            
            
        }
        //Si la busqueda fue perfecta, la ponemos por pantalla
        if(resultado!= null){
            ponerNodos(resultado, 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

