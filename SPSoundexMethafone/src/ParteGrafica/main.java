    /**
     * Asiga el texto por pantalla, dependiendo del tipo
     *  1- para Autocompletador
     *  2-resultados Soundex
     * 
     * Genera el resultado leyendo el Array Ingresado
     * y lo establece con setText en el panel indicado
     * @param nodos ArrayList de Nodos
     * @param tipo Si es un resultado de Autocompletador o de SOundex
     */
    public void ponerNodos(ArrayList<Nodo> nodos, int tipo){
        
        
        if(nodos == null){
            if(tipo == 1){
                jTextArea1.setText("");
                jTextArea1.repaint();
            }
            else{
                jTextArea2.setText("");
                jTextArea2.repaint();
            }
            return;
        }
        
        System.out.println("Entramos a ponerNodos");
        if (nodos.size() == 1 && nodos != null){
            nodoSoundex = nodos.get(0);
            System.out.println("Nuestreo nodo elejido es..." + nodoSoundex.getTexto());
        }else{
            nodoSoundex = null;
        }
        
        
        String Mensaje = "";
        
        int tam = (Integer)(jSpinner1.getValue());
        if(tam > nodos.size())tam = nodos.size();
        /*
        
            JOptionPane.showMessageDialog(this,"La longitud supera a los resultados");
        }
        */
        for (int i = 0; i < tam; i++) {
            
            Mensaje += nodos.get(i).getTexto()+ "\n";
        }
        
        if(tipo == 1){
                jTextArea1.setText(Mensaje);

                jTextArea1.repaint();
            }
            else{
                jTextArea2.setText(Mensaje);
                jTextArea2.repaint();
            }
        Mensaje = "";
        for (int i = 0; i < historialNodos.size(); i++) {
            Mensaje+= historialNodos.get(i).getTexto()+ "\n" +"\n";
            
        }
        jTextArea3.setText(Mensaje);
        jTextArea3.repaint();
        
        
    }
